/**
 * Класс, представляющий время суток, выраженное в количестве секунд,
 * прошедших с начала суток.
 */
class Time {
    int seconds;  // Количество секунд, прошедших с начала суток

    /**
     * Конструктор для создания времени, заданного количеством секунд.
     * Время будет приведено к диапазону от 0 до 86399 секунд (в пределах 24 часов).
     *
     * @param seconds Количество секунд, прошедших с начала суток.
     */
    public Time(int seconds) {
        // Используем остаток от деления на 86400 (количество секунд в сутках),
        // чтобы гарантировать, что время не выйдет за пределы 24 часов
        this.seconds = seconds % 86400;
    }

    /**
     * Переопределение метода toString для представления времени в текстовом формате.
     * Формат отображения: "ЧЧ:ММ:СС", где ЧЧ — часы, ММ — минуты, СС — секунды.
     * Если часы, минуты или секунды меньше 10, они будут отображаться с ведущим нулем.
     *
     * @return Строковое представление времени в формате "ЧЧ:ММ:СС".
     */
    @Override
    public String toString() {
        int hours = seconds / 3600;        // Количество полных часов
        int minutes = (seconds % 3600) / 60; // Количество полных минут
        int remainingSeconds = seconds % 60;  // Оставшиеся секунды

        return String.format("%d:%02d:%02d", hours, minutes, remainingSeconds);
    }
}
